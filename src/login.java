    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        //buat statement try catch yang menghubungkan table login di db ke inputan frame login
        //sistem menyiapkan record yang ada di table
        try {
        String sql="Select * from login where username='"+txtusername.getText()+"' and password='"+txtpassword.getText()+"'";
        pst=koneksi.prepareStatement(sql);
        rst=pst.executeQuery();
        //buat statement if untuk inputan benar seperti di record table lalu show msg welcome lalu ke frame transaksi, else untuk inputan salah show msg salah
        if (rst.next()) {
            if(txtusername.getText().equals(rst.getString("username"))&&txtpassword.getText().equals(rst.getString("password")))
            {  
                JOptionPane.showMessageDialog(null, "Selamat Datang '"+txtusername.getText()+"'"); 
                new transaksi().setVisible(true);
                dispose();
            }
                } else {JOptionPane.showMessageDialog(null, "Username / password salah");
                txtusername.setText("");
                txtpassword.setText("");
                }
        }catch (Exception e) {JOptionPane.showMessageDialog(null, e);}
    }//GEN-LAST:event_btnloginActionPerformed

